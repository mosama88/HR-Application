    /**
     * الحصول على اسم دولة وكودها بدون تكرار
     */
    public static function uniqueCountry()
    {
        $available = array_diff_key(static::$currencies, static::$usedCurrencies);

        if (empty($available)) {
            // Reset if exhausted
            static::$usedCurrencies = [];
            $available = static::$currencies;
        }

        // اختر عشوائي من الدول المتاحة
        $currencyName = array_rand($available);
        $currencySymbol = static::$currencies[$currencyName];

        // سجل كأنها تم استخدامها
        static::$usedCurrencies[$currencyName] = true;

        return [
            'name' => $currencyName,
            'currency_symbol' => $currencySymbol,
        ];
    }
